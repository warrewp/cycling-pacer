import tempfile
import webbrowser
import os


def _fmt_time(seconds):
    h = int(seconds // 3600)
    m = int((seconds % 3600) // 60)
    s = int(seconds % 60)
    if h > 0:
        return f"{h}h {m:02d}m {s:02d}s"
    return f"{m}m {s:02d}s"


def _group_zones(segments):
    if not segments:
        return []

    groups = []
    current_zone = segments[0]['zone_label']
    current_start = 0

    for i, seg in enumerate(segments):
        if seg['zone_label'] != current_zone:
            groups.append((current_start, i - 1, current_zone))
            current_zone = seg['zone_label']
            current_start = i

    groups.append((current_start, len(segments) - 1, current_zone))

    merged = []
    for start, end, zone in groups:
        zone_segs = segments[start:end + 1]
        total_dist = sum(s['distance_m'] for s in zone_segs)
        avg_power = sum(s['power_w'] * s['time_s'] for s in zone_segs) / max(1, sum(s['time_s'] for s in zone_segs))
        avg_speed = sum(s['speed_ms'] * s['time_s'] for s in zone_segs) / max(1, sum(s['time_s'] for s in zone_segs))
        total_time = sum(s['time_s'] for s in zone_segs)
        avg_gradient = sum(s['gradient'] * s['distance_m'] for s in zone_segs) / max(1, total_dist)

        merged.append({
            'zone': zone,
            'start_km': zone_segs[0]['cumulative_m'] / 1000,
            'end_km': (zone_segs[-1]['cumulative_m'] + zone_segs[-1]['distance_m']) / 1000,
            'distance_km': total_dist / 1000,
            'avg_gradient': avg_gradient,
            'avg_power': avg_power,
            'avg_speed_kmh': avg_speed * 3.6,
            'total_time': total_time,
        })

    return merged


