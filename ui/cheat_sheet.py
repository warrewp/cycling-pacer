def open_cheat_sheet(result, params, course_name="Course"):
    html = generate_cheat_sheet(result, params, course_name)
    with tempfile.NamedTemporaryFile('w', suffix='.html', delete=False) as f:
        f.write(html)
        path = f.name
    webbrowser.open('file://' + os.path.abspath(path))
    return path
